
import requests
import traceback
from datetime import datetime

LOG_FILE = "super_nari_full_test_log.txt"

def log(message):
    with open(LOG_FILE, "a", encoding="utf-8") as f:
        f.write(f"[{datetime.now()}] {message}\n")

def test_feature(name, method, url, data=None):
    try:
        log(f"ğŸ” Testing {name}...")
        if method == "POST":
            res = requests.post(url, json=data)
        elif method == "GET":
            res = requests.get(url)
        else:
            log(f"âŒ Unsupported method for {name}")
            return
        assert res.status_code == 200, f"{name} failed with status {res.status_code}"
        log(f"âœ… {name} passed")
    except Exception as e:
        log(f"âŒ {name} failed: {e}")
        log(traceback.format_exc())

